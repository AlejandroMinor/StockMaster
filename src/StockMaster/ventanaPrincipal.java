    public void actualizarDatos() {

        try {
            String SQL = "update item set nombre=?,descripcion=?,precio_publico=?, precio_distribuidor=?, stock=? where iditem=?";
            PreparedStatement pst = con.prepareStatement(SQL);
            String idSeleccionada = tablaArticulos.getValueAt(tablaArticulos.getSelectedRow(), 0).toString();
            pst.setString(1, Articulo.getText());
            pst.setString(2, descripcion.getText());
            pst.setFloat(3, Float.parseFloat(precioPublico.getText()));
            pst.setFloat(4, Float.parseFloat(precioDistribuidor.getText()));
            pst.setInt(5, Integer.parseInt(stock.getText()));
            pst.setString(6, idSeleccionada);

            pst.execute();
            JOptionPane.showMessageDialog(null, "Cambios guardados correctamente");

        } catch (Exception e) {
            System.out.println("Error" + e);
        }

    }


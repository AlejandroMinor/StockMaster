    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        imgLogoSeguricam.requestFocus();
        fondoGuardar.setVisible(false);
        btnGuardar.setVisible(false);
        mensajeBienvenida.setText("Te damos la bienvenida, " + datosUsuario.getNombreUsuario() + " " + datosUsuario.getApellidosUsuario());
        String puesto = datosUsuario.getpuesto().toUpperCase();

        if (puesto.equals("EMPLEADO")) {
            btnUsuarios.setVisible(false);
            btnUsuariosFondo.setVisible(false);
            btnEditar.setVisible(false);
            fondoEditar.setVisible(false);
        } else {
            btnUsuarios.setVisible(true);
            btnUsuariosFondo.setVisible(true);
            btnEditar.setVisible(true);
            fondoEditar.setVisible(true);
        }
    }//GEN-LAST:event_formWindowOpened


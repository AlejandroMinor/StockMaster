    private void btnGenerarPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarPDFActionPerformed

        JFileChooser selectorCarpeta = new JFileChooser();

        selectorCarpeta.setDialogTitle("Seleccione la carpeta para guardar el reporte");
        selectorCarpeta.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        selectorCarpeta.setAcceptAllFileFilterUsed(false);
        int seleccion = selectorCarpeta.showOpenDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {

            File carpetaSelecionada = selectorCarpeta.getSelectedFile();

            Date date = new Date();
            DateFormat hourdateFormat = new SimpleDateFormat("HH.mm.ss dd-MM-yyyy");
            String historial = hourdateFormat.format(date);

            destino = carpetaSelecionada.toString() + "\\REPORTE " + historial + ".pdf";
            JOptionPane.showMessageDialog(this, "El reporte se guardo en " + carpetaSelecionada.toString());
            try {
                generarPDF();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(reportes.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "No se guardo ningun reporte");
        }


    }//GEN-LAST:event_btnGenerarPDFActionPerformed


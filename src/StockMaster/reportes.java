    public void mostrarDatosArticulos() {

        String[] titulos = {"ID", "Articulo", "P. Publico", "P. Distribuidor", "Stock"};
        String[] registros = new String[5];

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        String SQL = "select * from item";

        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);

            while (rs.next()) {

                registros[0] = rs.getString("iditem");
                registros[1] = rs.getString("nombre");
                registros[2] = rs.getString("precio_publico");
                registros[3] = rs.getString("precio_distribuidor");
                registros[4] = rs.getString("stock");

                modelo.addRow(registros);

            }

            tablaArticulos.setModel(modelo);
            tablaArticulos.setDefaultEditor(Object.class, null);
            establecerColumnas();
        } catch (Exception e) {
            System.out.println("Error" + e);

        }

    }

    public void mostrarDatosVentas() {

        String[] titulos = {"ID", "Fecha", "Total", "Vendedor"};
        String[] registros = new String[4];

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        String SQL = "select * from venta";

        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);

            while (rs.next()) {

                registros[0] = rs.getString("idventa");
                registros[1] = rs.getString("fecha");
                registros[2] = rs.getString("total");
                String idusuario = rs.getString("user_iduser");

                String SQL2 = "select * from user where iduser = '" + idusuario + "'";
                Statement st2 = con.createStatement();
                ResultSet rs2 = st2.executeQuery(SQL2);
                while (rs2.next()) {
                    registros[3] = rs2.getString("nombre") + " " + rs2.getString("apellidos");
                }

                modelo.addRow(registros);

            }

            tablaVentas.setModel(modelo);
            tablaVentas.setDefaultEditor(Object.class, null);
            establecerColumnas();
        } catch (Exception e) {
            System.out.println("Error" + e);

        }

    }

    public void generarPDF() throws FileNotFoundException {

        String SQL = "select * from item";

        PdfWriter writer = new PdfWriter(destino);
        PdfDocument pdf = new PdfDocument(writer);
        Document document = new Document(pdf, PageSize.A4);
        Table table = new Table(new float[]{1, 6, 1, 1, 1});
        table.setWidth(500);

        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            table.addHeaderCell(new Cell().add(new Paragraph("ID")));
            table.addHeaderCell(new Cell().add(new Paragraph("Articulo")));
            table.addHeaderCell(new Cell().add(new Paragraph("Precio Publico")));
            table.addHeaderCell(new Cell().add(new Paragraph("Precio Distribuidor")));
            table.addHeaderCell(new Cell().add(new Paragraph("Stock")));
            while (rs.next()) {

                table.addCell(new Cell().add(new Paragraph(rs.getString("iditem"))));
                table.addCell(new Cell().add(new Paragraph(rs.getString("nombre"))));
                table.addCell(new Cell().add(new Paragraph(rs.getString("precio_publico"))));
                table.addCell(new Cell().add(new Paragraph(rs.getString("precio_distribuidor"))));
                table.addCell(new Cell().add(new Paragraph(rs.getString("stock"))));
            }

            document.add(table);
            document.close();
            establecerColumnas();
        } catch (Exception e) {
            System.out.println("Error" + e);

        }

    }

    public void establecerColumnas() {

        tablaArticulos.getColumnModel().getColumn(0).setPreferredWidth(25);
        tablaArticulos.getColumnModel().getColumn(1).setPreferredWidth(250);
        tablaArticulos.getColumnModel().getColumn(2).setPreferredWidth(50);
        tablaArticulos.getColumnModel().getColumn(3).setPreferredWidth(50);
        tablaArticulos.getColumnModel().getColumn(4).setPreferredWidth(25);
    }

    public void seleccionReporte() {

        if (btnReporteVentas.isSelected()) {
            panelVentas.setVisible(true);
            panelArticulos.setVisible(false);
            btnGenerarPDF.setVisible(false);
            mostrarDatosVentas();
        }
        if (btnReporteArticulos.isSelected()) {
            panelVentas.setVisible(false);
            panelArticulos.setVisible(true);
            btnGenerarPDF.setVisible(true);
            mostrarDatosArticulos();

        }
    }


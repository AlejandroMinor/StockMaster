    private void btnGuardarPasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarPasswordMouseClicked

        if (Nombre.getText().equals("") | ID.getText().equals("") | apellidos.getText().equals("") | contrasena.getText().equals("") | confirmarContrasena.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Para continuar debe rellenar todos los campos", "Alerta", JOptionPane.WARNING_MESSAGE);
        } else {

            if (contrasena.getText().equals(confirmarContrasena.getText())) {
                int result = JOptionPane.showConfirmDialog(null, " ¿Está seguro que desea realizar los cambios? ", "",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if (result == JOptionPane.YES_OPTION) {

                    Nombre.setEditable(false);
                    Nombre.setBackground(new java.awt.Color(5, 86, 172));
                    apellidos.setEditable(false);
                    apellidos.setBackground(new java.awt.Color(5, 86, 172));
                    ID.setEditable(false);
                    ID.setBackground(new java.awt.Color(5, 86, 172));
                    puesto.setEnabled(false);
                    puesto.setBackground(new java.awt.Color(5, 86, 172));
                    puesto.setVisible(false);
                    puestoText.setVisible(true);

                    fondoGuardar.setVisible(false);
                    btnGuardar.setVisible(false);
                    btnGuardarPassword.setVisible(false);

                    cambiarContrasena.setVisible(false);
                    contrasena.setVisible(false);
                    textContrasena.setVisible(false);
                    confirmarContrasena.setVisible(false);
                    textCofirmarContrasena.setVisible(false);
                    cambiarContrasena.setSelected(false);

                    actualizarDatosContrasenia();
                    mostrarDatos();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden", "Alerta", JOptionPane.WARNING_MESSAGE);
            }

        }


    }//GEN-LAST:event_btnGuardarPasswordMouseClicked


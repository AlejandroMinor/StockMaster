    public void mostrarDatos() {

        String[] titulos = {"ID Usuario", "Nombre", "Apellidos", "Puesto"};
        String[] registros = new String[5];

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        String SQL = "select * from user";

        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);

            while (rs.next()) {

                registros[0] = rs.getString("idUser");
                registros[1] = rs.getString("Nombre");
                registros[2] = rs.getString("apellidos");
                registros[3] = rs.getString("puesto");

                modelo.addRow(registros);

            }

            tablaUsuarios.setModel(modelo);
            tablaUsuarios.setDefaultEditor(Object.class, null);

        } catch (Exception e) {
            System.out.println("Error" + e);

        }

    }

    public void actualizarDatos() {

        try {
            String SQL = "update user set Nombre=?,apellidos=?,puesto=? where iduser=?";
            PreparedStatement pst = con.prepareStatement(SQL);
            String idSeleccionada = tablaUsuarios.getValueAt(tablaUsuarios.getSelectedRow(), 0).toString();
            pst.setString(1, Nombre.getText());
            pst.setString(2, apellidos.getText());
            int seleccionPuesto = puesto.getSelectedIndex();
            pst.setString(3, puesto.getItemAt(seleccionPuesto));
            pst.setString(4, idSeleccionada);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Cambios guardados correctamente");

        } catch (Exception e) {
            System.out.println("Error" + e);
        }

    }

